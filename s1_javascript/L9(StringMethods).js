// string methods in javascript 

// 1.) length -> Returns the length of the string
let myString = "Harry is nice";
console.log(myString.length);

// 2.) indexOf() -> Returns the index of the string
console.log(myString.indexOf("is"));

// 3.) lastIndexOf() -> Returns the last index of the string
console.log(myString.lastIndexOf("r"));

// 4.) slice() -> Returns the sliced string
console.log(myString.slice(0, 5));
// o -> index, 5->len

// 5.) replace() -> Replaces the string
console.log(myString.replace("Harry", "Rohan"));


